#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	div;

	div = 1;
	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			n *= -1;
			ft_putchar_fd('-', fd);
		}
		while (n / div >= 10)
			div *= 10;
		while (div != 0)
		{
			ft_putchar_fd((n / div % 10 + '0'), fd);
			div = div / 10;
		}
	}
}
